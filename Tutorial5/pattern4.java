
public class pattern4 {
    public static void main(String[] args) {
        int m=4;
         // no of rows =
         
        for (int i=m; i>=1 ;i--){ // for( m to  )
            for (int j=i; j>=1 ;j--){ // for(i to 1 )
                System.out.print("*" + " ");
            }
            System.out.println();
        }
        

    }
    
}
