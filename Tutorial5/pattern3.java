

public class pattern3 {
    public static void main(String[] args) {
        int m=4;
         // no of rows =
         
        for (int i=1; i<=m ;i++){ // for( i to m)
            for (int j=1; j<=i ;j++){ // for(j to i )
                System.out.print("*" + " ");
            }
            System.out.println();
        }
        

    }
    
    
}
