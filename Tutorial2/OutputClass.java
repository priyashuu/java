class OutputClass {
    
    public static void main(String args[]) {
        // Prints "Hello world" with a newline at the end
        System.out.println("Hello world");
        
        // Prints "Hello world" without adding a newline at the end
        System.out.print("Hello world\n");
        
        // Prints "Hello world" again without a newline
        System.out.print("Hello world");
    }
}
